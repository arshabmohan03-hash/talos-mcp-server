#!/usr/bin/env node
const fs = require('node:fs');
const path = require('node:path');

const serverRoot = path.resolve(__dirname, '..');
const sourceRoot = process.env.TALOS_SOURCE_ROOT
  ? path.resolve(process.env.TALOS_SOURCE_ROOT)
  : path.resolve(serverRoot, '..');
const runtimeRoot = path.join(serverRoot, 'app-runtime');

const keyNames = [
  'CEREBRAS_API_KEY',
  'CEREBRAS_API_KEY_SECONDARY',
  'GROQ_API_KEYS',
  'OPENALEX_API_KEY',
  'CORE_API_KEY',
  'SEMANTIC_SCHOLAR_API_KEY',
  'ALERT_EMAIL',
  'ALERT_EMAIL_PASSWORD',
  'SMTP_HOST',
  'SMTP_PORT',
  'SECURITY_ALERT_EMAIL',
  'SLACK_WEBHOOK_URL',
];

function parseEnvFile(filePath) {
  const values = {};
  if (!fs.existsSync(filePath)) {
    return values;
  }

  const lines = fs.readFileSync(filePath, 'utf8').split(/\r?\n/);
  for (const rawLine of lines) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#') || !line.includes('=')) {
      continue;
    }
    const index = line.indexOf('=');
    const key = line.slice(0, index).trim();
    let value = line.slice(index + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"'))
      || (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    values[key] = value;
  }
  return values;
}

function pyString(value) {
  return JSON.stringify(value || '');
}

function writeLocalKeys(targetRoot, keys) {
  const appDir = path.join(targetRoot, 'app');
  if (!fs.existsSync(appDir)) {
    return false;
  }

  const target = path.join(appDir, 'local_test_keys.py');
  const body = [
    '"""Test-only hardcoded keys for standalone Talos MCP deployments.',
    '',
    'Generated by `npm run embed:test-keys`. Environment variables still override',
    'these values. Do not use this file for production secrets.',
    '"""',
    '',
    'TEST_KEYS = {',
    ...keyNames.map((name) => `    ${pyString(name)}: ${pyString(keys[name] || '')},`),
    '}',
    '',
  ].join('\n');

  fs.writeFileSync(target, body, 'utf8');
  return true;
}

const envValues = {
  ...parseEnvFile(path.join(sourceRoot, '.env')),
  ...process.env,
};
const keys = {};
for (const name of keyNames) {
  keys[name] = envValues[name] || '';
}

const wroteSource = writeLocalKeys(sourceRoot, keys);
const wroteRuntime = writeLocalKeys(runtimeRoot, keys);
const embedded = keyNames.filter((name) => keys[name]);

console.log(JSON.stringify({
  wroteSource,
  wroteRuntime,
  embedded,
}, null, 2));
